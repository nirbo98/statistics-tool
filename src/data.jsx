// ---- Chi-Square GOF ----
export function genChiGOF() {
  const cats = [["חום","ירוק","כחול","אפור"],["A","B","C","D"],["רוק","פופ","קלאסי","ג׳אז"],["כן","לא","אולי","לא יודע"]][rnd(0,3)];
  const k = cats.length;
  const equalP = rnd(0,1) === 1;
  const probs = equalP ? cats.map(() => 1/k) : (() => { const raw = cats.map(() => rnd(1,5)); const s = raw.reduce((a,b)=>a+b); return raw.map(x => x/s); })();
  const n = rnd(5,15) * 10;
  const obs = cats.map(() => rnd(Math.floor(n/k*0.4), Math.floor(n/k*1.6)));
  const obsSum = obs.reduce((a,b)=>a+b);
  obs[obs.length-1] += n - obsSum;
  const exp = probs.map(p => n * p);
  const chi = obs.reduce((s, o, i) => s + Math.pow(o - exp[i], 2) / exp[i], 0);
  const df = k - 1;
  const tc = chiCritical(df, 0.05);
  return { type: "gof", names: cats, n, probs, obs, exp, chi: parseFloat(chi.toFixed(3)), df, tc, reject: chi > tc, equalP };
}

// ---- Chi-Square Independence ----
export function genChiInd() {
  const rN = ["גברים","נשים"], cN = ["בעד","נגד","אדיש"];
  const r = rN.length, c = cN.length;
  const obs = [];
  for (let i = 0; i < r; i++) { obs.push([]); for (let j = 0; j < c; j++) obs[i].push(rnd(8, 35)); }
  const N = obs.flat().reduce((a,b)=>a+b);
  const rT = obs.map(row => row.reduce((a,b)=>a+b));
  const cT = Array(c).fill(0); obs.forEach(row => row.forEach((v,j) => cT[j] += v));
  const exp = obs.map((row,i) => row.map((_,j) => rT[i]*cT[j]/N));
  let chi = 0; obs.forEach((row,i) => row.forEach((o,j) => chi += Math.pow(o-exp[i][j],2)/exp[i][j]));
  const df = (r-1)*(c-1);
  const tc = chiCritical(df, 0.05);
  return { type: "ind", rN, cN, r, c, obs, exp, N, rT, cT, chi: parseFloat(chi.toFixed(3)), df, tc, reject: chi > tc };
}

// ---- Binomial ----
export function genBinom() {
  const n = rnd(8, 20);
  const pOpts = [0.5, 1/3, 0.25, 0.3, 0.4, 0.6]; const p = pOpts[rnd(0, pOpts.length - 1)];
  const q = 1 - p;
  const dirs = ["greater", "less", "two"]; const dir = dirs[rnd(0, 2)];
  const mu = n * p; const sigma = Math.sqrt(n * p * q);
  let k;
  if (dir === "greater") k = rnd(Math.ceil(mu + 1), n);
  else if (dir === "less") k = rnd(0, Math.floor(mu - 1));
  else k = rnd(0, 1) === 0 ? rnd(0, Math.max(0, Math.floor(mu - 2))) : rnd(Math.ceil(mu + 2), n);
  const probTable = []; for (let i = 0; i <= n; i++) probTable.push(binomProb(n, i, p));
  const canZ = n * p >= 5 && n * q >= 5;
  let pVal;
  if (dir === "greater") pVal = probTable.slice(k).reduce((a, b) => a + b, 0);
  else if (dir === "less") pVal = probTable.slice(0, k + 1).reduce((a, b) => a + b, 0);
  else { const each = probTable.map(pr => pr <= probTable[k] + 1e-10 ? pr : 0); pVal = each.reduce((a, b) => a + b, 0); }
  let Z = null;
  if (canZ) {
    let corr = dir === "greater" ? -0.5 : dir === "less" ? 0.5 : (k < mu ? 0.5 : -0.5);
    Z = (k + corr - mu) / sigma;
  }
  return { type: "binom", n, p, q, k, mu, sigma, probTable, canZ, Z, dir, pVal, reject: pVal < 0.05, pLabel: p === 1/3 ? "1/3" : p === 0.25 ? "1/4" : p.toString() };
}

// ---- T-tests ----
