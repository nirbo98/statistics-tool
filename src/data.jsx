export function genRegression() {
  const n=rnd(10,30),xbar=rndF(20,80,1),ybar=rndF(20,80,1);
  const sx=rndF(3,15,2),sy=rndF(3,15,2),r=rndF(-0.9,0.9,3);
  const b=r*sy/sx, a=ybar-b*xbar, r2=r*r;
  const sest=sy*Math.sqrt(n*(1-r2)/(n-2));
  const xP=rndF(xbar-2*sx,xbar+2*sx,1), yP=a+b*xP;
  return { type:"regression", n,xbar,ybar,sx,sy,r,b,a,r2,sest,xP,yP };
}

// --- CI ---
export function genCI() {
  const types=["single_known","single_unknown","paired","independent"];
  const type=types[rnd(0,3)]; const alpha=[0.05,0.01,0.1][rnd(0,2)]; const conf=(1-alpha)*100;
  const zVals={0.05:1.96,0.01:2.576,0.1:1.645};
  if(type==="single_known"){
    const n=rnd(30,80),xbar=rndF(40,100,1),sigma=rndF(5,20,1);
    const z=zVals[alpha],se=sigma/Math.sqrt(n),margin=z*se;
    return {type,n,xbar,sigma,alpha,conf,z,se,margin,lo:xbar-margin,hi:xbar+margin};
  }
  if(type==="single_unknown"){
    const n=rnd(10,40),xbar=rndF(40,100,1),shat=rndF(5,20,1),df=n-1;
    const t=df>30?2.0:2.1; const se=shat/Math.sqrt(n),margin=t*se;
    return {type,n,xbar,shat,alpha,conf,df,t,se,margin,lo:xbar-margin,hi:xbar+margin};
  }
  if(type==="paired"){
    const n=rnd(8,25),dbar=rndF(-5,10,2),sd=rndF(2,8,2),df=n-1;
    const t=df>30?2.0:2.1; const se=sd/Math.sqrt(n),margin=t*se;
    return {type,n,dbar,sd,alpha,conf,df,t,se,margin,lo:dbar-margin,hi:dbar+margin};
  }
  const n1=rnd(10,30),n2=rnd(10,30),x1=rndF(40,80,1),x2=rndF(40,80,1);
  const s1=rndF(5,15,1),s2=rndF(5,15,1),df=n1+n2-2;
  const t=2.0; const sp2=((n1-1)*s1*s1+(n2-1)*s2*s2)/df;
  const se=Math.sqrt(sp2*(1/n1+1/n2)),diff=x1-x2,margin=t*se;
  return {type,n1,n2,x1,x2,s1,s2,alpha,conf,df,t,sp2,se,diff,margin,lo:diff-margin,hi:diff+margin};
}

// --- Power (Binom) ---
export function genPower() {
  const n=rnd(6,12); const p0Opts=[0.5,1/3,1/4,0.3,0.4]; const p0=p0Opts[rnd(0,p0Opts.length-1)];
  const dirs=["right","left"]; const dir=dirs[rnd(0,1)]; const alpha=0.05;
  const pH0=[]; for(let k=0;k<=n;k++)pH0.push(binomProb(n,k,p0));
  let rejR=[];
  if(dir==="right"){let c=0;for(let k=n;k>=0;k--){c+=pH0[k];if(c<=alpha)rejR.push(k);else break;}}
  else{let c=0;for(let k=0;k<=n;k++){c+=pH0[k];if(c<=alpha)rejR.push(k);else break;}}
  const actAlpha=rejR.reduce((s,k)=>s+pH0[k],0);
  let p1; if(dir==="right")p1=Math.min(p0+0.2+Math.random()*0.2,0.9); else p1=Math.max(p0-0.2-Math.random()*0.2,0.1);
  p1=parseFloat(p1.toFixed(2));
  const pH1=[]; for(let k=0;k<=n;k++)pH1.push(binomProb(n,k,p1));
  const power=rejR.reduce((s,k)=>s+pH1[k],0); const beta=1-power;
  return {type:"power",n,p0,p1,dir,alpha,actAlpha,pH0,pH1,rejR,power,beta,p0Label:p0===1/3?"1/3":p0===1/4?"1/4":p0.toString()};
}
