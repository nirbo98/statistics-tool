export function genTInd() {
  const n1 = rnd(10, 30); const n2 = rnd(10, 30);
  const x1 = rnd(40, 80); const x2 = x1 + rnd(-12, 12);
  const s1 = rnd(5, 18); const s2 = rnd(5, 18);
  const F = Math.max(s1, s2) ** 2 / Math.min(s1, s2) ** 2;
  const dfF1 = (s1 >= s2 ? n1 : n2) - 1; const dfF2 = (s1 >= s2 ? n2 : n1) - 1;
  const Fc = fCritical(dfF1, dfF2, 0.05);
  const eq = F < Fc;
  let df, se, sp2 = null;
  if (eq) {
    sp2 = ((n1 - 1) * s1 * s1 + (n2 - 1) * s2 * s2) / (n1 + n2 - 2);
    se = Math.sqrt(sp2) * Math.sqrt(1 / n1 + 1 / n2);
    df = n1 + n2 - 2;
  } else {
    se = Math.sqrt(s1 * s1 / n1 + s2 * s2 / n2);
    const num = Math.pow(s1*s1/n1 + s2*s2/n2, 2);
    const den = Math.pow(s1*s1/n1, 2)/(n1-1) + Math.pow(s2*s2/n2, 2)/(n2-1);
    df = Math.floor(num / den);
  }
  const t = (x1 - x2) / se;
  const tc = tCritical(df, 0.05);
  const d = sp2 ? Math.abs(x1 - x2) / Math.sqrt(sp2) : Math.abs(x1 - x2) / Math.sqrt((s1*s1 + s2*s2) / 2);
  return { type: "ind", n1, n2, x1, x2, s1, s2, F: parseFloat(F.toFixed(3)), Fc: parseFloat(Fc.toFixed(3)), eq, df, se: parseFloat(se.toFixed(4)), sp2: sp2 ? parseFloat(sp2.toFixed(2)) : null, t: parseFloat(t.toFixed(3)), tc, reject: Math.abs(t) > tc, d: parseFloat(d.toFixed(3)) };
}

// ---- F-test ----
export function genFTest() {
  const n1 = rnd(10, 30); const n2 = rnd(10, 30);
  const s1 = rnd(4, 20); const s2 = rnd(4, 20);
  const sMax = Math.max(s1, s2); const sMin = Math.min(s1, s2);
  const nMax = s1 >= s2 ? n1 : n2; const nMin = s1 >= s2 ? n2 : n1;
  const F = (sMax * sMax) / (sMin * sMin);
  const df1 = nMax - 1; const df2 = nMin - 1;
  const Fc = fCritical(df1, df2, 0.05);
  const reject = F > Fc;
  return { type: "ftest", n1, n2, s1, s2, sMax, sMin, nMax, nMin, F: parseFloat(F.toFixed(3)), df1, df2, Fc: parseFloat(Fc.toFixed(3)), reject };
}

