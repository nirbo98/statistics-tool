// ============ BASIC HELPERS ============
export function rnd(a, b) { return Math.floor(Math.random() * (b - a + 1)) + a; }
export function rndF(a, b, d) { return parseFloat((Math.random() * (b - a) + a).toFixed(d)); }
function factorial(n) { if (n <= 1) return 1; let r = 1; for (let i = 2; i <= n; i++) r *= i; return r; }
function comb(n, k) { if (k > n || k < 0) return 0; return factorial(n) / (factorial(k) * factorial(n - k)); }
function binomProb(n, k, p) { return comb(n, k) * Math.pow(p, k) * Math.pow(1 - p, n - k); }

function assignRanks(values) {
  const indexed = values.map((v, i) => ({ val: v, origIdx: i }));
  indexed.sort((a, b) => a.val - b.val);
  const result = new Array(values.length);
  let i = 0;
  while (i < indexed.length) {
    let j = i;
    while (j < indexed.length && indexed[j].val === indexed[i].val) j++;
    const avg = (i + 1 + j) / 2;
    for (let k = i; k < j; k++) result[indexed[k].origIdx] = avg;
    i = j;
  }
  return result;
}

