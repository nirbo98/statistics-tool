// --- T-test independent ---
export function genTInd() {
  const n1=rnd(10,30),n2=rnd(10,30);
  const x1=rndF(40,80,1),x2=rndF(40,80,1),s1=rndF(5,15,1),s2=rndF(5,15,1);
  const F=Math.max(s1*s1,s2*s2)/Math.min(s1*s1,s2*s2);
  const eq=F<3;
  const df=eq?n1+n2-2:Math.min(n1,n2)-1;
  let se,sp2=null;
  if(eq){sp2=((n1-1)*s1*s1+(n2-1)*s2*s2)/(n1+n2-2);se=Math.sqrt(sp2*(1/n1+1/n2));}
  else{se=Math.sqrt(s1*s1/n1+s2*s2/n2);}
  const t=(x1-x2)/se; const tc=2.0; const reject=Math.abs(t)>tc;
  return { type:"tInd", n1,n2,x1,x2,s1,s2,F,eq,df,se,sp2,t,tc,reject };
}

// --- Wilcoxon Paired ---
export function genWilcoxonPaired() {
  let sA,sB,nzPairs,zeroIdx;
  const n=rnd(7,10);
  do {
    sA=Array.from({length:n},()=>rnd(1,15));
    sB=Array.from({length:n},()=>rnd(1,15));
    nzPairs=[]; zeroIdx=[];
    sA.forEach((a,i)=>{const d=a-sB[i];if(d===0)zeroIdx.push(i);else nzPairs.push({a,b:sB[i],d,idx:i});});
  } while(nzPairs.length<5);
  const effN=nzPairs.length;
  const absD=nzPairs.map(x=>Math.abs(x.d));
  const ranks=assignRanks(absD);
  const signedR=ranks.map((r,i)=>nzPairs[i].d>0?r:-r);
  const Tp=signedR.filter(r=>r>0).reduce((s,r)=>s+r,0);
  const Tm=signedR.filter(r=>r<0).reduce((s,r)=>s+Math.abs(r),0);
  const T=Math.min(Tp,Tm);
  const muT=effN*(effN+1)/4; const sigT=Math.sqrt(effN*(effN+1)*(2*effN+1)/24);
  const Z=sigT>0?(T+0.5-muT)/sigT:0;
  return { type:"wilcPaired", n,effN,sA,sB,nzPairs,zeroIdx,absD,ranks,signedR,Tp,Tm,T,muT,sigT,Z };
}

// --- Mann-Whitney ---
export function genMannWhitney() {
  const nA=rnd(5,7),nB=rnd(5,7);
  const gA=Array.from({length:nA},()=>rnd(1,20));
  const gB=Array.from({length:nB},()=>rnd(1,20));
  const combined=[
    ...gA.map((v,i)=>({val:v,group:"A",idx:i})),
    ...gB.map((v,i)=>({val:v,group:"B",idx:i})),
  ].sort((a,b)=>a.val-b.val);
  const allR=assignRanks(combined.map(c=>c.val));
  const rA=[],rB=[];
  combined.forEach((item,i)=>{if(item.group==="A")rA.push(allR[i]);else rB.push(allR[i]);});
  const WA=rA.reduce((s,r)=>s+r,0); const WB=rB.reduce((s,r)=>s+r,0);
  const isAS=nA<=nB; const W=isAS?WA:WB;
  const nS=Math.min(nA,nB),nL=Math.max(nA,nB),totN=nA+nB;
  const muW=nS*(totN+1)/2; const sigW=Math.sqrt(nS*nL*(totN+1)/12);
  const Z=sigW>0?(W+0.5-muW)/sigW:0;
  return { type:"mannWhitney", nA,nB,gA,gB,combined,allR,rA,rB,WA,WB,W,nS,nL,muW,sigW,Z,totN };
}

// --- Correlation ---
export function genCorrPearson() {
  const n=rnd(15,50); const r=rndF(-0.85,0.85,3);
  const df=n-2; const t=r*Math.sqrt(df)/Math.sqrt(1-r*r);
  const tc=df>30?2.0:2.1;
  return { type:"corrPearson", n,r,df,t,tc,reject:Math.abs(t)>tc };
}

export function genCorrCompare() {
  const n1=rnd(20,60),n2=rnd(20,60);
  const r1=rndF(-0.7,0.8,3),r2=rndF(-0.7,0.8,3);
  const rp1=0.5*Math.log((1+r1)/(1-r1)),rp2=0.5*Math.log((1+r2)/(1-r2));
  const se=Math.sqrt(1/(n1-3)+1/(n2-3));
  const Z=(rp1-rp2)/se;
  return { type:"corrCompare", n1,n2,r1,r2,rp1,rp2,se,Z,reject:Math.abs(Z)>1.96 };
}

